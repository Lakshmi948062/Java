public class Char
{
    public static void main(String[] args) 
    {
        char x = 'l';
        System.out.println("The character is: " + x);

        char y = '1'; //Number included in the single quote it treats as a char
        System.out.println("The number is: " + y);

        char z = '\u0190';
        System.out.println("The charcters is : "+ z);
    }
}